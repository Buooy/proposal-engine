				<h4>User Centric Leader</h4> 
				<p>The user centric leader functions in an industry where technology's primary role is in driving revenue, but is either largely undifferentiated from another competitor's competitive advantage or easily replicated by another competitor e.g. e-commerce and media.</p>
				</p>As the CTO, he/she is focused on engaging the end user to understand user behavior and feedback, and develop technological strategies with the intentions of driving revenue.</p>
				<br>
			</div>
			<div class="col-xs-12">
				<h4>Innovative Leader</h4> 
				<p>The innovative leader functions in a technology company at the forefront of the technological landscape, building platforms with groundbreaking solutions, e.g Salesforce, Google, Oracle.</p>
				</p>As the CTO, he/she foresees the potential problems that technology can solve profitably in the next one to two decades. He/she develops new technologies from scratch, and build platforms that other organizations and individuals can leverage upon.</p>
