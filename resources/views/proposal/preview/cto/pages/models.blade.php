				<h4>Operational Leader</h4> 
				<p>The operational leader functions in an organisation where technology plays a crucial supporting role and has lower direct impact on revenue e.g. financial and educational institutions.</p>
				</p>As the CTO, he/she is tasked to manage and maintain an existing infrastructure, focusing on ensuring that mission-critical systems are monitored and to enhance the operational capabilities of frontline and supporting staff.</p>
				<br>
			</div>
			<div class="col-xs-12">
				<h4>Visionary Leader</h4> 
				<p>The visionary leader is crucial to an organization that utilizes technology to engage users relating to a common goal or vision e.g. NPOs, Government</p>
				<p>As the CTO, he/she is seen as a strategist who can assess the tech landscape, look into the future, identify the use cases/scenarios and develop the technological roadmaps that will facilitate the overall strategy.</p>
